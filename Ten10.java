public class Ten10 {
    public static void main(String[] args) {
        System.out.println("Using for loop decrement:");
        //using for loop
        for(int i=10; i > 1; i--){
            System.out.println("The value of i is: "+i);
        }    
        
        System.out.println("");
        System.out.println("Using while loop increment:");
        //using while loop
        int j=0;

        while(j < 11){
            System.out.println("The value of i is: "+j);
            j++;
        }
    }
}
